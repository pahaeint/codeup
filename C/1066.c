#include <stdio.h>

int main() {
    int n,m,l;
    scanf("%d %d %d",&n,&m,&l);
    if(n%2==0){
        printf("even\n");
    }
    else{
        printf("odd\n");
    }
    if(m%2==0){
        printf("even\n");
    }
    else{
        printf("odd\n");
    }
    if(l%2==0){
        printf("even\n");
    }
    else{
        printf("odd\n");
    }
    return 0;
}